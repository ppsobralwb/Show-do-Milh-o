const premios = [0, 1000, 2000, 3000, 4000, 5000, 10000, 20000, 30000, 40000, 50000, 100000, 200000, 300000, 400000, 500000, 1000000];

let pergunta = document.getElementById('pergunta');
let escolha1 = document.getElementById('btn1');
